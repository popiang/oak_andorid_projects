package com.popiang.firstkotlinproject

fun main(args: Array<String>) {

    var number1:Double = 8.4;
    var number2:Double = 4.2;
    var result:Double = 0.0;

    result = number1 + number2;
    println(result)

    result = number1 - number2;
    println(result)

    result = number1 * number2;
    println(result)

    result = number1 / number2;
    println(result)

    result = number1 % number2;
    println(result)

}